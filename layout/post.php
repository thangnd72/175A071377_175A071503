                        <p>(TLU) - Trong các ngày từ ngày 3 đến 7-12 tại Trường Đại học Bách Khoa - Đại học Đà Nẵng
                            đã diễn ra các cuộc thi: cuộc thi Olympic Tin học sinh viên Việt Nam lần thứ 28
