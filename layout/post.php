                            2019) và cuộc thi đối kháng Procon.Năm 2019, kỳ thi Olympic Tin học sinh viên toàn quốc
                            có sự tham dự của hơn 700 sinh viên công nghệ thông tin xuất sắc đến từ 100 trường ĐH,
                            CĐ từ khắp mọi miền trên cả nước và 17 đội tuyển quốc tế đến từ các nước Nhật Bản, Hàn
                            Quốc, Singapore, Trung Quốc, Indonesia, Myanmar... Trong đó, có các đội tuyển đến từ các
                            trường mạnh và nổi tiếng trong lĩnh vực CNTT như Pekin University, ĐH Quốc gia Kyoto, ĐH
                            Korea, ĐH Quốc gia Singapore, ĐH Quốc gia Indonesia.

                            Đội tuyển Olympic Tin học Trường Đại học Thủy lợi gồm 7 sinh viên do thầy Trương Xuân
                            Nam làm trưởng đoàn đã dự thi 3 nội dung: Chuyên tin học, Không chuyên tin học (dành cho
                            sinh viên không chuyên ngành CNTT và sinh viên năm nhất ngành CNTT) và ICPC khu vực châu
                            Á. Đây là lần đầu tiên Nhà trường tham dự thi kỳ thi ICPC.

                            Với những nỗ lực của cả thầy và trò, cuộc thi đã đem về cho Nhà trường những kết quả
                            đáng mừng. Cụ thể:

                            1. SV Từ Việt Thảo, lớp 60HT đạt giải Ba khối Chuyên tin học;

                            2. SV Tưởng Thị Hồng Nhung, lớp 58HT đạt giải Khuyến khích khối Chuyên tin học;

                            3. SV Trần Quốc Toản, lớp 61PM2 đạt giải Khuyến khích khối Không chuyên tin học;

                            4. Đội tuyển ICPC lần đầu tham dự xếp vị trí 60/115 đội tuyển Quốc tế và các đội mạnh từ
                            các trường ĐH lớn trong nước; xếp thứ 46 trong số các đội tuyển từ Việt Nam; xếp thứ 20
                            trong số các trường ĐH ở Việt Nam.

                            Đây là lần đầu tiên đội tuyển của Trường Đại học Thủy lợi tham gia kỳ thi quốc tế khu
                            vực Châu Á ICPC và giành được kết quả rất đáng khích lệ.

                            Xin chúc mừng thành tích của đoàn và của cá nhân các thí sinh tham dự kỳ thi Olympic Tin
